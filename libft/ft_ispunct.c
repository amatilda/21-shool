int		ft_ispunct(int c)
{
	if (c <= ' ' || c > 126)
		return (0);
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (0);
	if (c >= 0x30 && c <= 0x39)
		return (0);
	return (1);
}
