char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	int		tmp;

	i = 0;
	while (src[i] != 0)
	{
		tmp = src[i];
		dest[i] = tmp;
		i++;
	}
	dest[i] = 0;
	return (dest);
}
