#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*out;

	out = 0;
	while (s[0] != 0)
	{
		if (s[0] == c)
			out = (char *)s;
		s++;
	}
	if (s[0] == c)
		out = (char *)s;
	return (out);
}
